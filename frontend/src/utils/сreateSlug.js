export default function createSlug(productName) {
  // Удаляем специальные символы, заменяем пробелы на дефисы, приводим к нижнему регистру
  return productName
    .normalize("NFD") // разложение на символы и диакритические знаки
    .replace(/[\u0300-\u036f]/g, "") // удаление диакритических знаков
    .replace(/[^\w\s-]/g, "") // удаление специальных символов, кроме дефисов
    .trim() // удаление пробелов с начала и конца строки
    .replace(/\s+/g, "-") // замена пробелов и последовательностей пробелов на дефис
    .toLowerCase(); // приведение к нижнему регистру
}

// Пример использования
const slug = createSlug("Название Продукта 123 $$$");
console.log(slug); // "название-продукта-123"
